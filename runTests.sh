#!/bin/bash
make 
make clean

echo "##############################################################################################"
echo "################################## GOOD FILES"

for nazwa in good/* ; do
	echo "##############################################################################################"
	echo "################################## File: $nazwa"
	cat $nazwa
	echo ""
	echo "################################## Interpretation: $nazwa"
	./Interpreter $nazwa
	echo ""
done

echo "##############################################################################################"
echo "################################## BAD FILES"

for nazwa in bad/* ; do
	echo "##############################################################################################"
	echo "################################## File: $nazwa"
	cat $nazwa
	echo ""
	echo "################################## Interpretation: $nazwa"
	./Interpreter $nazwa
	echo ""
done